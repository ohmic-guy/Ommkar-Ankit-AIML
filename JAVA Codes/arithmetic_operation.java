class arithmetic_operation {
    public static void main(String[] args) {
		int a=5;
		int b=10;
		int c= a+b;
		int d=a-b;
		int e=a*b;
		int f=a/b;
		System.out.println("Addition= "+c);
		System.out.println("Subtraction= "+d);
		System.out.println("Product= "+e);
		System.out.println("Division= "+f);
    }
}